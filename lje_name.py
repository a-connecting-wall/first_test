def cat_test():
    print("🐾 당신을 고양이로 치면?\n")
    print("Y/N으로 솔직하게 대답해줘!\n")

    traits = {
        "독립": 0,
        "소심": 0,
        "예민": 0,
        "쿨": 0,
        "몽상": 0,
        "선택적 애교": 0,
        "집순이": 0
    }

    questions = [
        ("혼자 있는 시간이 꼭 필요하다", "독립"),
        ("먼저 다가가는 것보단 다가와줬으면 좋겠다", "소심"),
        ("맘에 안 들면 티가 나는 편이다", "예민"),
        ("관심은 좋지만 너무 과하면 피곤하다", "쿨"),
        ("갑작스러운 변화나 낯선 환경은 불편하다", "소심"),
        ("가끔 아무 이유 없이 멍 때리는 걸 좋아한다", "몽상"),
        ("좋아하는 사람한테만 애교가 터진다", "선택적 애교"),
        ("이불 속이 세상에서 제일 안전하다", "집순이")
    ]

    for q, trait in questions:
        while True:
            ans = input(f"{q} (Y/N): ").strip().upper()
            if ans in ["Y", "N"]:
                break
            print("Y 또는 N으로 대답해주세요!")

        if ans == "Y":
            traits[trait] += 1

    # 유형별 판단 로직
    if traits["쿨"] >= 2 or traits["독립"] >= 2:
        result = "🐾 러시안블루형 – 쿨내 진동 도시묘"
        comment = "“건들지 마… 나중에 기분 좋으면 다가갈게.”"
        match = "먼치킨형 (조용한 안정지향)"
        not_match = "스핑크스형 (과한 관심, 피곤해함)"
        desc = "- 독립적이고 예민하지만 정들면 묘하게 정이 많다.\n- 적당한 거리와 혼자만의 시간이 꼭 필요하다."
    elif traits["소심"] >= 2 or traits["선택적 애교"] >= 2:
        result = "🐾 아비시니안형 – 소심+애교 콤보냥"
        comment = "“어… 나한테 다가와도… 괜찮을지도…?”"
        match = "러시안블루형 (적당한 거리 유지해주는 스타일)"
        not_match = "스핑크스형 (처음부터 들이대서 당황)"
        desc = "- 낯가림이 심하고 천천히 정드는 스타일.\n- 믿음이 생기면 과하게 애교 많아짐."
    elif traits["몽상"] >= 1 and traits["집순이"] >= 1:
        result = "🐾 먼치킨형 – 뒹굴뒹굴 집순이 냥"
        comment = "“이불 밖은 위험해요… 나 그냥 누워있을래…”"
        match = "러시안블루형 (조용한 방 분위기 찰떡)"
        not_match = "스핑크스형 (자꾸 놀자고 해서 피곤)"
        desc = "- 루틴 사랑, 자극 싫어함.\n- 안정감 있는 환경에서 제일 편안함."
    elif sum(traits.values()) <= 2:
        result = "🐾 스핑크스형 – 무한관심형 애정폭발냥"
        comment = "“어디 가냐! 나 봐라! 나 보라고!!”"
        match = "코숏형 (기분 따라 호흡 잘 맞는 친구)"
        not_match = "러시안블루형 (쿨한 타입과 충돌)"
        desc = "- 외로움을 싫어하고 관심과 교감이 생명.\n- 표현 많고 활발한 성격!"
    else:
        result = "🐾 코숏형 – 묘성미 넘치는 반반냥"
        comment = "“다정하다가 훅~ 변함. 예측은 하지 마세요”"
        match = "스핑크스형 (기분 따라 어울림)"
        not_match = "아비시니안형 (불안정함에 피로감)"
        desc = "- 감정 기복이 있어 예측이 어려운 타입.\n- 매력 있지만 다루기 까다로운 스타일."

    # 출력
    print("\n✨ 테스트 결과 ✨")
    print(result)
    print(f"{comment}\n")
    print("📍 성격 요약:")
    print(desc)
    print(f"\n❤️ 당신과 잘 맞는 고양이: {match}")
    print(f"⚠️ 잘 맞지 않는 고양이: {not_match}")

# 실행
cat_test()




